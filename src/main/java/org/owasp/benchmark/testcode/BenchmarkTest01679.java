@WebServlet("/BenchmarkTest01679")
public class BenchmarkTest01679 extends HttpServlet {
	
	private static final long serialVersionUID = 1L;
	
	@Override
	public void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doPost(request, response);
	}

	@Override
	public void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
	
		String param = "";
		java.util.Enumeration<String> headerNames = request.getHeaderNames();
		if (headerNames.hasMoreElements()) {
			param = headerNames.nextElement(); // just grab first element
		}
		
		
		// Chain a bunch of propagators in sequence
		String a21918 = param; //assign
		StringBuilder b21918 = new StringBuilder(a21918);  // stick in stringbuilder
		b21918.append(" SafeStuff"); // append some safe content
		b21918.replace(b21918.length()-"Chars".length(),b21918.length(),"Chars"); //replace some of the end content
		java.util.HashMap<String,Object> map21918 = new java.util.HashMap<String,Object>();
		map21918.put("key21918", b21918.toString()); // put in a collection
		String c21918 = (String)map21918.get("key21918"); // get it back out
		String d21918 = c21918.substring(0,c21918.length()-1); // extract most of it
		String e21918 = new String( new sun.misc.BASE64Decoder().decodeBuffer( 
		    new sun.misc.BASE64Encoder().encode( d21918.getBytes() ) )); // B64 encode and decode it
		String f21918 = e21918.split(" ")[0]; // split it on a space
		org.owasp.benchmark.helpers.ThingInterface thing = org.owasp.benchmark.helpers.ThingFactory.createThing();
		String bar = thing.doSomething(f21918); // reflection
		
		
		// FILE URIs are tricky because they are different between Mac and Windows because of lack of standardization.
		// Mac requires an extra slash for some reason.
		String startURIslashes = "";
        if (System.getProperty("os.name").indexOf("Windows") != -1)
	        if (System.getProperty("os.name").indexOf("Windows") != -1)
	        	startURIslashes = "/";
	        else startURIslashes = "//";

		try {
			java.net.URI fileURI = new java.net.URI("file:" + startURIslashes 
				+ org.owasp.benchmark.helpers.Utils.testfileDir.replace('\\', '/').replace(' ', '_') + bar);
			new java.io.File(fileURI);
		} catch (java.net.URISyntaxException e) {
			throw new ServletException(e);
		}
	}
}
