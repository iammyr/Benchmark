		java.util.HashMap<String,Object> map16455 = new java.util.HashMap<String,Object>();
		map16455.put("key16455", b16455.toString()); // put in a collection
		String c16455 = (String)map16455.get("key16455"); // get it back out
		String d16455 = c16455.substring(0,c16455.length()-1); // extract most of it
		String e16455 = new String( new sun.misc.BASE64Decoder().decodeBuffer( 
		    new sun.misc.BASE64Encoder().encode( d16455.getBytes() ) )); // B64 encode and decode it
		String f16455 = e16455.split(" ")[0]; // split it on a space
